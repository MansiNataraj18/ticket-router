SELECT ut.id, p.permission_id
FROM user_type ut, permission p
WHERE ut.name = 'ADMIN'
  AND p.permission_name IN ('VIEW_ALL_TICKETS', 'MANAGE_USERS', 'DELETE_LOW_PRIORITY_TICKET');

INSERT INTO user_type_permissions (user_type_id, permission_id)
SELECT ut.id, p.permission_id
FROM user_type ut, permission p
WHERE ut.name = 'CUSTOMER'
  AND p.permission_name IN ('SUBMIT_TICKET', 'VIEW_OWN_TICKETS');

INSERT INTO user_type_permissions (user_type_id, permission_id)
SELECT ut.id, p.permission_id
FROM user_type ut, permission p
WHERE ut.name IN ('ACCOUNTS_STAFF', 'ENGINEERING_STAFF', 'IAM_STAFF', 'PRODUCT_STAFF', 'SUPPORT_STAFF')
  AND p.permission_name IN ('VIEW_DEPARTMENT_TICKETS', 'UPDATE_TICKET_STATUS', 'DELETE_REJECTED_TICKET');


-- ---------- Migrate users.role -> users.user_type_id ----------
-- Existing ADMIN/USER accounts map 1:1 to the new ADMIN/CUSTOMER types.
-- Existing AGENT accounts are folded into SUPPORT_STAFF (Customer Service
-- Team) as the closest equivalent of a generic "staff" role.

ALTER TABLE users ADD COLUMN user_type_id BIGINT;

UPDATE users u
SET user_type_id = ut.id
FROM user_type ut
WHERE (u.role = 'ADMIN' AND ut.name = 'ADMIN')
   OR (u.role = 'USER'  AND ut.name = 'CUSTOMER')
   OR (u.role = 'AGENT' AND ut.name = 'SUPPORT_STAFF');

ALTER TABLE users ALTER COLUMN user_type_id SET NOT NULL;

ALTER TABLE users
    ADD CONSTRAINT fk_users_user_type
    FOREIGN KEY (user_type_id)
    REFERENCES user_type (id);

ALTER TABLE users DROP COLUMN role;


-- ---------- Ticket status workflow ----------

ALTER TABLE ticket ADD COLUMN status VARCHAR(20) NOT NULL DEFAULT 'PENDING';

ALTER TABLE ticket
    ADD CONSTRAINT ticket_status_check
    CHECK (status IN ('PENDING', 'ACCEPTED', 'REJECTED', 'IN_PROGRESS', 'COMPLETED'));
