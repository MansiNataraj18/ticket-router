-- Introduces a proper RBAC schema (user_type / permission / mapping table)
-- to replace the fixed users.role enum, and adds a ticket status workflow
-- for department staff.

-- ---------- Catalog tables ----------

CREATE TABLE user_type (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(50) NOT NULL UNIQUE,
    description VARCHAR(255),
    department_team_name VARCHAR(100)
);

CREATE TABLE permission (
    permission_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    permission_name VARCHAR(100) NOT NULL UNIQUE,
    permission_description VARCHAR(255)
);

-- Many-to-many mapping: which permissions each user type has.
CREATE TABLE user_type_permissions (
    user_type_id BIGINT NOT NULL REFERENCES user_type (id),
    permission_id BIGINT NOT NULL REFERENCES permission (permission_id),
    PRIMARY KEY (user_type_id, permission_id)
);


